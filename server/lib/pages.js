

function homeOpen() {
   const element = document.getElementById("home");
   if (element.className == "home-close") {
     element.className = "home-open";
   } else {
     element.className = "home-open";
   }
 }

 function homeClose() {
   const element = document.getElementById("home");
   if (element.className == "home-open") {
     element.className = "home-close";
   } else {
     element.className = "home-close";
   }
 }
 
